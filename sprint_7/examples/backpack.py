import pprint


def knapsack(weights: list, prices: list, W: int) -> int:
    n: int = len(weights)
    dp: list[list[int]] = [[0 for _ in range(W + 1)] for _ in range(n + 1)]

    for i in range(1, n + 1):
        for w in range(W + 1):
            if weights[i - 1] <= w:
                dp[i][w] = max(dp[i - 1][w], dp[i - 1][w - weights[i - 1]] + prices[i - 1])
            else:
                dp[i][w] = dp[i - 1][w]
    pprint.pprint(dp)
    print()
    return dp[n][W]


